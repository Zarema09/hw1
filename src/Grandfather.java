public class Grandfather {
    private String name;
    private int age;
    private String gender;


    public Grandfather(String name, int age, String gender) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }
    public Grandfather() {

    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
    public String getGender() {
        return gender;
    }
}